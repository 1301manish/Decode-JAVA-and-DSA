package Week_1_Basics;

public class TypeCasting {
    public static void main(String[] args) {
        //Implicit type casting
        int x = 2;
        double y = x;
        System.out.println(y);

        //Explicit type casting
         double a =3.5;
         int b = (int)a;
        System.out.println(b);
    }
}
