package Week_1_Basics;

public class ModulusOperator {
    public static void main(String[] args) {
        System.out.println(3%4); //when a<b answer=a.
        System.out.println(21%-4); // sign of b does not matter sign of a comes in answer.
        System.out.println(-21%4); // sign of b does not matter sign of a comes in answer.
        System.out.println(-21%-4);// sign of b does not matter sign of a comes in answer.

    }
}
