package Week_3_Pattern;

public class Rotated_Number_Square {
    public static void main(String[] args) {
        for (int i = 1; i <=4; i++) {      //Rows
            for (int j = 1; j <=5; j++) {  //Columns
                System.out.print(i+" ");
            }
            System.out.println();
        }
    }
}
