public class Star_Square {
    public static void main(String[] args) {
        for (int i = 1; i <=4; i++) {      //Rows
            for (int j = 1; j <=4; j++) {  //Columns
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}
